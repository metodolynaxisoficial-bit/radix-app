const MS_DAY = 24 * 60 * 60 * 1000

function iso(d: Date): string {
  return d.toISOString()
}

function addDays(base: number, days: number): Date {
  return new Date(base + days * MS_DAY)
}

/** Início do dia local (00:00) */
function startOfLocalDay(d: Date): Date {
  const x = new Date(d)
  x.setHours(0, 0, 0, 0)
  return x
}

/** Fim do dia local para comparação de prazo de inscrição */
function endOfLocalDay(d: Date): Date {
  const x = new Date(d)
  x.setHours(23, 59, 59, 999)
  return x
}

export function diasRestantesInscricao(edital: EditalPSS): number {
  if (!edital.data_fim_inscricao) return 999
  const fim = endOfLocalDay(new Date(edital.data_fim_inscricao))
  if (Number.isNaN(fim.getTime())) return 999
  const diff = fim.getTime() - Date.now()
  return Math.ceil(diff / MS_DAY)
}

export function diasRestantesProcesso(edital: EditalPSS): number {
  if (!edital.data_fim_processo) return 999
  const fim = endOfLocalDay(new Date(edital.data_fim_processo))
  if (Number.isNaN(fim.getTime())) return 999
  const agora = new Date()
  const diff = fim.getTime() - agora.getTime()
  return Math.ceil(diff / MS_DAY)
}

export function formatarMoeda(valor: number): string {
  return new Intl.NumberFormat('pt-BR', {
    style: 'currency',
    currency: 'BRL',
    minimumFractionDigits: 2,
    maximumFractionDigits: 2,
  }).format(valor)
}

export function formatarPopulacao(valor: number): string {
  return `${new Intl.NumberFormat('pt-BR').format(valor)} hab.`
}

