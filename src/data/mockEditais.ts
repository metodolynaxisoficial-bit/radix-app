/** Instância estável durante a sessão; recarregar a página recalcula datas */
export const editaisMock: EditalPSS[] = buildEditais()

export function getEditalById(id: string): EditalPSS | undefined {
  return editaisMock.find((e) => e.id === id)
}

/** PSS ativos = Aberto + Em breve */
export function isPssAtivo(edital: EditalPSS): boolean {
  return edital.status === 'Aberto' || edital.status === 'Em breve'
}

export function contagemPorRegiao(list: EditalPSS[] = editaisMock): Record<EditalPSS['regiao'], number> {
  const acc: Record<string, number> = {
    Norte: 0,
    Nordeste: 0,
    'Centro-Oeste': 0,
    Sudeste: 0,
    Sul: 0,
    'Distrito Federal': 0,
  }
  for (const e of list) {
    if (isPssAtivo(e)) acc[e.regiao]++
  }
  return acc as Record<EditalPSS['regiao'], number>
}

export function novosEstaSemana(list: EditalPSS[] = editaisMock, now: number = Date.now()): number {
  const limite = now - 7 * MS_DAY
  return list.filter((e) => new Date(e.data_inicio_inscricao).getTime() >= limite).length
}

export function parseDataBR(iso: string): string {
  if (!iso) return '—'
  const d = new Date(iso)
  if (Number.isNaN(d.getTime())) return '—'
  const dd = String(d.getDate()).padStart(2, '0')
  const mm = String(d.getMonth() + 1).padStart(2, '0')
  const yyyy = d.getFullYear()
  return `${dd}/${mm}/${yyyy}`
}

export function periodProgress(isoStart: string, isoEnd: string): number {
  if (!isoStart || !isoEnd) return 0
  const start = startOfLocalDay(new Date(isoStart)).getTime()
  const end = endOfLocalDay(new Date(isoEnd)).getTime()
  if (Number.isNaN(start) || Number.isNaN(end)) return 0
  const now = Date.now()
  if (now <= start) return 0
  if (now >= end) return 100
  return Math.round(((now - start) / (end - start)) * 100)
}
